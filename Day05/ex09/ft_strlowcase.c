char	*ft_strlowcase(char *str)
{
	char	*begin;

	begin = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str = *str + 32;
		}
		str++;
	}
	str = begin;
	return (str);
}
