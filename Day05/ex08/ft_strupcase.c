char	*ft_strupcase(char *str)
{
	char	*begin;

	begin = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = *str - 32;
		}
		str++;
	}
	str = begin;
	return (str);
}
